		<p style="text-align: justify;">Paul is a French chain of  bakery/café restaurants established in 1889 in the town of Croix, in Nord, by Charlemagne Mayot. Experience a charming bit of French frills at PAUL within the campus. The 120-year-old, family-run boulangerie combines the scrumptious hominess of the French countryside with the classiness of a Parisian café. Think rustic wood interiors, wrought-iron chairs, and smells of freshly baked bread wafting from the ovens.
Mornings mean egg dishes and on-point viennoiserie, such as croissant aux amandes — perfect with a chocolat chaud - while lunch leans towards light French classics (turkey croque-monsieur, gratin de saumon, etc.). And, of course, crepes, both sweet and savoury.
With so many choices, it's okay to linger, especially over French-inspired patisserie like creamy date éclairs. 
